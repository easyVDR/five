//-------------------------------------------------------------------------
//     MsgBody::MsgBody()
//-------------------------------------------------------------------------
MsgBody::MsgBody(BODY *theMsgBody)
       : myMsgBody(theMsgBody)
{
} // MsgBody::MsgBody()

//-------------------------------------------------------------------------
//     MsgBody::~MsgBody()
//-------------------------------------------------------------------------
MsgBody::~MsgBody()
{
} // MsgBody::~MsgBody()

//-------------------------------------------------------------------------
//     MsgBody::MsgBody()
//-------------------------------------------------------------------------
MsgBody::MsgBody(const MsgBody &that)
{
  myMsgBody = that.myMsgBody;
} // MsgBody::MsgBody()

//-------------------------------------------------------------------------
//     MsgBody::operator=()
//-------------------------------------------------------------------------
MsgBody &MsgBody::operator=(const MsgBody &that)
{
  if (this != &that)
  {
    myMsgBody = that.myMsgBody;
  } // if

  return *this;
} // MsgBody::operator=()

//-----------------------------------------------------------------------
//     MsgBody::dump()
//-----------------------------------------------------------------------
void MsgBody::dump() const
{
  Internal::dumpMsgBody(myMsgBody);
} // MsgBody::dumpMsgBody()

