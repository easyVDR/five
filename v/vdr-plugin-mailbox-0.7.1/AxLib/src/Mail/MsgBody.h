//=============================================================================
/** simply a wrapper for c-client's BODY-Pointer
 *
 *  No memory-management is done here, this class is simply to hide
 *  the c-client BODY-type in the implementation of Ax::Mail and not
 *  to have c-client types in the headers of Ax::Mail.
 */
class MsgBody
{
  public:
    //-------------------------------------------------------------------------
    //     MsgBody()
    //-------------------------------------------------------------------------
    MsgBody(BODY *theBody);

    //-------------------------------------------------------------------------
    //     ~MsgBody()
    //-------------------------------------------------------------------------
    ~MsgBody();

    //-------------------------------------------------------------------------
    //     MsgBody()
    //-------------------------------------------------------------------------
    /** copy constructor
     */
    MsgBody(const MsgBody &that);

    //-------------------------------------------------------------------------
    //     operator=()
    //-------------------------------------------------------------------------
    /** assignment operator
     */
    MsgBody &operator=(const MsgBody &that);

    //-------------------------------------------------------------------------
    //     isNull()
    //-------------------------------------------------------------------------
    bool isNull() const { return myMsgBody == 0; }

    //-------------------------------------------------------------------------
    //     get()
    //-------------------------------------------------------------------------
    /** Returns a pointer to the internal elt/MESSAGECACHE
     */
    BODY *get() const { return myMsgBody; }

    //-------------------------------------------------------------------------
    //     operator *()
    //-------------------------------------------------------------------------
    /** Returns a pointer to the internal elt/MESSAGECACHE
     */
    BODY *operator *() const { return myMsgBody; }

    //-------------------------------------------------------------------------
    //     operator ->()
    //-------------------------------------------------------------------------
    /** Returns a pointer to the internal elt/MESSAGECACHE
     *
     * allows direct access to members of myMsgBody
     */
    BODY *operator ->() const { return myMsgBody; }

    //-----------------------------------------------------------------------
    //     dump()
    //-----------------------------------------------------------------------
    /** Dump internal information to syslog
     */
    void dump() const;

  private:
    //-------------------------------------------------------------------------
    //     attributes
    //-------------------------------------------------------------------------
    BODY *myMsgBody;

}; // class MsgBody

