/** Menu to show the key-mappings for a given OsdMenu.
*/
template <typename TT>
class MenuHelpKeys : public Ax::Vdr::OsdMenu
{
    //-------------------------------------------------------------------------
    //     typedefs
    //-------------------------------------------------------------------------
    typedef Ax::Vdr::OsdMenu PARENT;
    typedef MenuHelpKeys     MYSELF;

  protected:
    //-------------------------------------------------------------------------
    //     enums
    //-------------------------------------------------------------------------
    enum AxEAction
    {
      A_FIRST          = PARENT::A_LAST
    , A_ACTIVATE_KEY   = A_FIRST
    , A_LAST
    };

  public:
    //-------------------------------------------------------------------------
    //     MenuHelpKeys()
    //-------------------------------------------------------------------------
    MenuHelpKeys(const Ax::Vdr::OsdMenuBase<TT> *theDocuMenu);

    //-------------------------------------------------------------------------
    //     ~MenuHelpKeys()
    //-------------------------------------------------------------------------
    virtual ~MenuHelpKeys();

    //-------------------------------------------------------------------------
    //     processAction()
    //-------------------------------------------------------------------------
    virtual eOSState processAction(const Ax::Vdr::Action &theAction, eOSState = osUnknown);

  private:
    //-------------------------------------------------------------------------
    //     attributes
    //-------------------------------------------------------------------------
    const Ax::Vdr::OsdMenuBase<TT> *myDocuMenu;

}; // class MenuHelpKeys

